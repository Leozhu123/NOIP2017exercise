template <typename T>
inline void read(T &t)
{
    int f = 0, c = getchar();
    t = 0;
    while (!isdigit(c))
        f |= c == '-', c = getchar();
    while (isdigit(c))
        t = t * 10 + c - 48, c = getchar();
    if (f)
        t = -t;
}
template <typename T, typename... Args>
inline void read(T &t, Args &... args)
{
    read(t);
    read(args...);
}

int main()
{
    int T;
    read(T);
    while (T--)
    {
        int n, s, t;
        read(n, s, t);
        int x, y, z;
        y = n - s;
        x = n - t;
        z = n - x - y;
        cout << max(x, y) + 1 << endl;
    }
}